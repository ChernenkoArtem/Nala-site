const selectSingle = document.querySelector('.__select');
const selectSingle_title = selectSingle.querySelector('.__select__title');
const selectSingle_labels = selectSingle.querySelectorAll('.__select__label');
const menuItemsList = document.querySelectorAll('.menu-item');
const isMobWidth = window.innerWidth < 800;
let lastScrollTop = 0;

window.onscroll = () => {
  const header = document.querySelector('.header');

  if (isMobWidth) {
    requestAnimationFrame(onscrollRocket);
    closeMenu();
    return;
  }

  if (document.documentElement.scrollTop > 110) {
    header.style.padding = '0 60px';
  } else {
    header.style.padding = '20px 60px';
  }
};

function setMenuClicks() {
  if (isMobWidth) {
    for (let i = 0; i < menuItemsList.length; i++) {
      menuItemsList[i].addEventListener('click', (evt) => {
        if (!evt.target.children[0]) {
          return;
        }
        const id =
          evt.target.children[0].attributes[0].nodeValue
            .split('')
            .slice(1, -1)
            .join()
            .replace(/,/g, '') + 'r';

        console.log(id);
        const element = document.getElementById(id);

        element.scrollIntoView();
        closeMenu();
      });
    }
  }
}

selectSingle_title.addEventListener('click', () => {
  if ('active' === selectSingle.getAttribute('data-state')) {
    selectSingle.setAttribute('data-state', '');
  } else {
    selectSingle.setAttribute('data-state', 'active');
  }
});

function setLang(value) {
  if (!value) {
    return;
  }
  selectSingle_title.textContent = value;
  selectSingle.setAttribute('data-state', '');

  localStorage.setItem('lang', value);

  for (let key in langArr) {
    let elem = document.querySelector('.lang-' + key);
    if (elem) {
      elem.innerHTML = langArr[key][value];
    }
  }
}

function setInitLang() {
  const storageSiteLang = localStorage.getItem('lang');
  setLang(storageSiteLang);

  for (let i = 0; i < selectSingle_labels.length; i++) {
    selectSingle_labels[i].addEventListener('click', (evt) => {
      setLang(evt.target.attributes.value.value);
    });
  }
}

function onscrollRocket() {
  const rocket = document.querySelector('.rocket');
  const roadMap = document.querySelector('.roadmap-container');
  const roadMapY = roadMap.scrollHeight;
  const top = roadMap.getBoundingClientRect().top;

  const st = window.pageYOffset || document.documentElement.scrollTop;
  if (st > lastScrollTop) {
    // downscroll code
    if (top < 0 && top > -roadMapY) {
      rocket.style.transform = 'rotate(180deg)';
      rocket.style.top = 50 + +top.toFixed(0) * -1 + 'px';
    }
  } else if (st < lastScrollTop) {
    // upscroll code
    if (top < 0 && top > -roadMapY) {
      rocket.style.transform = 'rotate(360deg)';
      rocket.style.top = 50 + +top.toFixed(0) * -1 + 'px';
    }
  }
  lastScrollTop = st <= 0 ? 0 : st;
}

function initApp() {
  menu();
  setInitLang();
  setMenuClicks();
}

document.addEventListener('load', initApp());
